#include "libft.h"

size_t	ft_lennumber(int n)
{
	size_t	index;

	index = 0;
	if (n == 0)
		index = 1;
	if (n < 0)
		index += 1;
	while (n != 0)
	{
		n = n / 10;
		index += 1;
	}
	return (index);
}
